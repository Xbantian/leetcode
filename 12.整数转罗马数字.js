/**
 * @param {number} num
 * @return {string}
 */
var intToRoman = function (num) {
  let mapping2 = {
      M: 1000,
      CM: 900,
      D: 500,
      CD: 400,
      C: 100,
      XC: 90,
      L: 50,
      XL: 40,
      X: 10,
      IX: 9,
      V: 5,
      IV: 4,
      I: 1,
    },
    res = '';
  for (const [key, val] of Object.entries(mapping2)) {
    let n = (num / val) | 0;
    res += key.repeat(n);
    num = num % val;
  }
  return res;
};
// @lc code=end
